    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
        // TODO add your handling code here:
        Crudsql objcrud = new Crudsql();
        Variables objvar = new Variables();
        
       
        objvar.setId(jTextField_resId.getText());
        objvar.setNombre(jTextField_res_nombre.getText());
        objvar.setDescripcion(jTextField_resdescrip.getText());
        objcrud.actualizar(objvar.getId(), objvar.getNombre(), objvar.getDescripcion());
        jTextField_resId.setText("");
        jTextField_res_nombre.setText("");
        jTextField_resdescrip.setText("");
    }//GEN-LAST:event_jButton_actualizarActionPerformed

